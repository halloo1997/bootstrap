        <p>De moeder van schrijver, columnist en NOS-presentator Hugo Borst is in een verpleegtehuis in Rotterdam overleden. Joke Borst was 89 jaar. Hugo schreef elke week over haar onder de kop "Mijn ma" in het AD-magazine. Zij leed aan alzheimer.</p>
        <p>Borsts woede over de behandeling die zijn moeder onderging, inspireerde hem en onderzoeker Carin Gaemers in 2016 tot het schrijven van een open brief aan de toenmalige staatssecretaris van Volksgezondheid, Van Rijn. Daarin veroordeelden zij het tekort aan personeel in de verpleegzorg en de vele administratieve taken die de verzorgenden moesten verrichten. De patiÃ«nten kregen daardoor niet de zorg en veiligheid die ze nodig hadden.</p>
        <p>Na aandrang van de Tweede Kamer maakte de Inspectie voor de Gezondheidszorg bekend dat 150 verpleeghuizen niet de zorg leverden waarop gerekend mocht worden.</p>
        <p><strong>Manifest</strong></p>
        <p>Borst en Gaemers schreven vervolgens het manifest Scherp op Ouderenzorg. Daarin werd de politiek opgeroepen om zo snel mogelijk goede zorg voor alle kwetsbare ouderen in verpleegtehuizen te waarborgen.</p>
        <p>Het manifest kreeg meer dan 100.000 steunbetuigingen en leidde ertoe dat het kabinet 2,1 miljard euro extra voor verpleeghuiszorg beschikbaar stelde.</p>
